package com.pojo;


public class ClientPOJO {

    private String clientId;
    private String clientName;
    private String clientToko;
    private String clienttempo;

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

	public void setClientToko(String clientToko) {
		this.clientToko = clientToko;
	}

	public String getClientToko() {
		return clientToko;
	}

	public void setClienttempo(String clienttempo) {
		this.clienttempo = clienttempo;
	}

	public String getClienttempo() {
		return clienttempo;
	}
}
